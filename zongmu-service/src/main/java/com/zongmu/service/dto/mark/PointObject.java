package com.zongmu.service.dto.mark;

import com.zongmu.service.dto.DTOObject;

public class PointObject extends DTOObject {
    private static final long serialVersionUID = -2400046607073745586L;

    private float x;

    private float y;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

}
